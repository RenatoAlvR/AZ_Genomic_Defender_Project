#!/usr/bin/env python3
"""
GenomeDefender — Combine Poisoned Datasets
==========================================
Merges any number of pre-computed poisoned datasets (data.npy + labels.npy)
into a single combined dataset for joint fine-tuning.

Works with 2 datasets, 10 datasets, or any number — no modification needed.

Each input directory must contain:
    data.npy    — preprocessed expression matrix (n_cells, n_genes)
    labels.npy  — binary labels (0=clean, 1=poisoned)

All datasets MUST have the same number of genes (n_genes dimension).
They must all have been generated from the same HVG set.

Output directory will contain:
    data.npy         — combined expression matrix (sum of all n_cells, n_genes)
    labels.npy       — combined binary labels
    source.npy       — integer array tracking which dataset each cell came from
    combine_meta.json — full provenance record

Usage:
    # Combine two datasets
    python combine_poisoned_datasets.py \
        --input_dirs data/GSE161529/poisoned_cancer_erasure/ \
                     data/GSE161529/poisoned_autoinmune/ \
        --output_dir data/GSE161529/poisoned_combined/

    # Combine many datasets
    python combine_poisoned_datasets.py \
        --input_dirs data/attack1/ data/attack2/ data/attack3/ data/attack4/ \
        --output_dir data/poisoned_combined_all/

    # Combine with balancing (undersample majority class to reach target ratio)
    python combine_poisoned_datasets.py \
        --input_dirs data/GSE161529/poisoned_cancer_erasure/ \
                     data/GSE161529/poisoned_autoinmune/ \
        --output_dir data/GSE161529/poisoned_combined/ \
        --balance    0.5
"""

import argparse
import json
import numpy as np
import pandas as pd
from pathlib import Path


def load_dataset(input_dir: Path) -> tuple:
    """Load data.npy and labels.npy from a directory.

    Returns:
        X:       np.ndarray (n_cells, n_genes)
        labels:  np.ndarray (n_cells,) — binary 0/1
        meta:    dict from attack_meta.json if present, else minimal dict
    """
    data_path   = input_dir / 'data.npy'
    labels_path = input_dir / 'labels.npy'
    meta_path   = input_dir / 'attack_meta.json'

    if not data_path.exists():
        raise FileNotFoundError(f"data.npy not found in {input_dir}")
    if not labels_path.exists():
        raise FileNotFoundError(
            f"labels.npy not found in {input_dir}. "
            f"Only datasets generated by attack scripts can be combined."
        )

    X      = np.load(data_path).astype(np.float32)
    labels = np.load(labels_path).astype(np.int32)

    if X.shape[0] != labels.shape[0]:
        raise ValueError(
            f"Shape mismatch in {input_dir}: "
            f"data.npy has {X.shape[0]} rows but labels.npy has {labels.shape[0]} rows."
        )

    meta = {'source_dir': str(input_dir), 'attack': 'unknown'}
    if meta_path.exists():
        with open(meta_path) as f:
            meta = json.load(f)
        meta['source_dir'] = str(input_dir)

    return X, labels, meta


def run_combine(args):
    output_dir  = Path(args.output_dir)
    output_dir.mkdir(parents=True, exist_ok=True)
    input_dirs  = [Path(d) for d in args.input_dirs]
    seed        = args.seed

    print(f"\n{'='*60}")
    print(f"GenomeDefender — Combine Poisoned Datasets")
    print(f"Input datasets: {len(input_dirs)}")
    print(f"Output:         {output_dir}")
    if args.balance is not None:
        print(f"Balance target: {args.balance:.0%} poisoned cells")
    print(f"{'='*60}\n")

    # ── Load all datasets ─────────────────────────────────────────────────────
    all_X      = []
    all_labels = []
    all_source = []   # integer index: which dataset did each cell come from
    dataset_metas = []

    n_genes_ref = None

    for i, input_dir in enumerate(input_dirs):
        print(f"Loading dataset {i+1}/{len(input_dirs)}: {input_dir.name} ...")
        X, labels, meta = load_dataset(input_dir)

        # Validate gene dimension consistency
        if n_genes_ref is None:
            n_genes_ref = X.shape[1]
        elif X.shape[1] != n_genes_ref:
            raise ValueError(
                f"Gene dimension mismatch: {input_dir} has {X.shape[1]} genes "
                f"but previous datasets have {n_genes_ref}. "
                f"All datasets must be generated from the same HVG set."
            )

        n_poison = labels.sum()
        print(f"  Shape:    {X.shape}")
        print(f"  Poisoned: {n_poison:,} / {len(labels):,} cells "
              f"({n_poison/len(labels)*100:.1f}%)")
        print(f"  Attack:   {meta.get('attack', 'unknown')}")

        all_X.append(X)
        all_labels.append(labels)
        all_source.append(np.full(len(labels), i, dtype=np.int32))
        dataset_metas.append(meta)

    # ── Concatenate ───────────────────────────────────────────────────────────
    print(f"\nConcatenating {len(input_dirs)} datasets...")
    X_combined      = np.concatenate(all_X,      axis=0)
    labels_combined = np.concatenate(all_labels, axis=0)
    source_combined = np.concatenate(all_source, axis=0)

    n_total  = len(labels_combined)
    n_poison = labels_combined.sum()
    print(f"Combined shape:   {X_combined.shape}")
    print(f"Total poisoned:   {n_poison:,} / {n_total:,} ({n_poison/n_total*100:.1f}%)")

    # ── Optional balancing ────────────────────────────────────────────────────
    if args.balance is not None:
        target_ratio = args.balance
        rng          = np.random.default_rng(seed)

        clean_idx   = np.where(labels_combined == 0)[0]
        poison_idx  = np.where(labels_combined == 1)[0]

        n_clean_now  = len(clean_idx)
        n_poison_now = len(poison_idx)

        # Target: n_poison / (n_poison + n_clean) = target_ratio
        # → n_clean_target = n_poison * (1 - target_ratio) / target_ratio
        n_clean_target = int(n_poison_now * (1 - target_ratio) / target_ratio)

        if n_clean_target > n_clean_now:
            # Would need to oversample clean — skip balancing, warn
            print(f"\n⚠  Balancing to {target_ratio:.0%} poisoned would require "
                  f"oversampling clean cells ({n_clean_target:,} needed, "
                  f"{n_clean_now:,} available). Skipping balancing.")
        else:
            print(f"\nBalancing: keeping {n_clean_target:,} clean cells "
                  f"(was {n_clean_now:,}) to reach {target_ratio:.0%} poison ratio...")
            selected_clean = rng.choice(clean_idx, n_clean_target, replace=False)
            keep_idx       = np.sort(np.concatenate([selected_clean, poison_idx]))

            X_combined      = X_combined[keep_idx]
            labels_combined = labels_combined[keep_idx]
            source_combined = source_combined[keep_idx]

            n_total  = len(labels_combined)
            n_poison = labels_combined.sum()
            print(f"After balancing:  {X_combined.shape}")
            print(f"Poisoned ratio:   {n_poison:,} / {n_total:,} "
                  f"({n_poison/n_total*100:.1f}%)")

    # ── Shuffle ───────────────────────────────────────────────────────────────
    print("\nShuffling combined dataset...")
    rng  = np.random.default_rng(seed)
    perm = rng.permutation(len(labels_combined))
    X_combined      = X_combined[perm]
    labels_combined = labels_combined[perm]
    source_combined = source_combined[perm]

    # ── Save ──────────────────────────────────────────────────────────────────
    print(f"\nSaving to {output_dir}...")
    np.save(output_dir / 'data.npy',   X_combined)
    np.save(output_dir / 'labels.npy', labels_combined)
    np.save(output_dir / 'source.npy', source_combined)

    # source_map.csv — human readable: which cell came from which dataset
    pd.DataFrame({
        'cell_index':   np.arange(len(labels_combined)),
        'source_dataset': [input_dirs[s].name for s in source_combined],
        'label':         labels_combined,
    }).to_csv(output_dir / 'source_map.csv', index=False)

    # Provenance metadata
    meta = {
        'n_datasets_combined': len(input_dirs),
        'seed':                seed,
        'balance_target':      args.balance,
        'n_total_cells':       int(n_total),
        'n_poisoned_cells':    int(n_poison),
        'n_clean_cells':       int(n_total - n_poison),
        'poison_fraction':     round(float(n_poison / n_total), 4),
        'n_genes':             int(X_combined.shape[1]),
        'datasets': [
            {
                'index':      i,
                'path':       str(d),
                'name':       d.name,
                'attack':     dataset_metas[i].get('attack', 'unknown'),
                'n_cells':    int(len(all_labels[i])),
                'n_poisoned': int(all_labels[i].sum()),
            }
            for i, d in enumerate(input_dirs)
        ],
    }
    with open(output_dir / 'combine_meta.json', 'w') as f:
        json.dump(meta, f, indent=2)

    # ── Summary ───────────────────────────────────────────────────────────────
    print(f"\n{'='*60}")
    print("COMBINE SUMMARY")
    print(f"{'='*60}")
    print(f"Datasets merged:  {len(input_dirs)}")
    for i, d in enumerate(input_dirs):
        n_p = int(all_labels[i].sum())
        n_c = len(all_labels[i])
        print(f"  [{i}] {d.name:45s} {n_c:>8,} cells  ({n_p/n_c*100:.1f}% poisoned)")
    print(f"\nCombined total:   {n_total:,} cells")
    print(f"Poisoned:         {n_poison:,} ({n_poison/n_total*100:.1f}%)")
    print(f"Clean:            {n_total - n_poison:,} ({(n_total-n_poison)/n_total*100:.1f}%)")
    print(f"\nOutput files:")
    print(f"  data.npy       — combined expression matrix {X_combined.shape}")
    print(f"  labels.npy     — binary labels (0=clean, 1=poisoned)")
    print(f"  source.npy     — integer array (which dataset each cell came from)")
    print(f"  source_map.csv — human-readable provenance per cell")
    print(f"  combine_meta.json")
    print(f"\n⚠  Use with --incremental --base_weights for fine-tuning.")
    print(f"✅  Done: {output_dir}")


if __name__ == '__main__':
    parser = argparse.ArgumentParser(
        description='Combine any number of poisoned datasets for joint fine-tuning.',
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog="""
Examples:
  # Two datasets
  python combine_poisoned_datasets.py \\
      --input_dirs data/poisoned_cancer_erasure/ data/poisoned_autoinmune/ \\
      --output_dir data/poisoned_combined/

  # Four datasets
  python combine_poisoned_datasets.py \\
      --input_dirs data/attack_a/ data/attack_b/ data/attack_c/ data/attack_d/ \\
      --output_dir data/poisoned_combined_all/

  # With 50/50 balance (equal clean/poisoned cells)
  python combine_poisoned_datasets.py \\
      --input_dirs data/poisoned_cancer_erasure/ data/poisoned_autoinmune/ \\
      --output_dir data/poisoned_combined_balanced/ \\
      --balance 0.5
        """
    )
    parser.add_argument(
        '--input_dirs', nargs='+', required=True,
        help='One or more directories containing data.npy and labels.npy. '
             'Pass as space-separated list. Minimum 2 recommended.'
    )
    parser.add_argument(
        '--output_dir', required=True,
        help='Output directory for the combined dataset.'
    )
    parser.add_argument(
        '--balance', type=float, default=None,
        help='Optional: target fraction of poisoned cells after combining '
             '(e.g. 0.5 for 50%% poisoned). Undersamples the clean class. '
             'If not set, all cells from all datasets are kept as-is.'
    )
    parser.add_argument(
        '--seed', type=int, default=42,
        help='Random seed for shuffling and balancing (default: 42).'
    )
    args = parser.parse_args()

    if len(args.input_dirs) < 2:
        print("⚠  Warning: only one input directory provided. "
              "combine_poisoned_datasets.py is designed to merge multiple datasets. "
              "Proceeding anyway...")

    run_combine(args)